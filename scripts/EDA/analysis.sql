--- Find total number of customers by countries
SELECT 
country,
COUNT(customer_id) AS total_customers 
FROM dbo.[gold.dim_customers]
GROUP BY country
ORDER BY COUNT(customer_id) DESC

---Find total customer by gender 
SELECT 
gender,
COUNT(customer_id) AS total_customersASPERgender
FROM dbo.[gold.dim_customers]
GROUP BY gender
ORDER BY COUNT(customer_id) DESC

---Find total products by category 
SELECT 
category,
COUNT(product_number) AS total_productsPERcategory 
FROM dbo.[gold.dim_products]
GROUP BY category
ORDER BY COUNT(product_number) DESC

---What is the average costs in each category?
SELECT 
category,
AVG(cost) AS AvergeCostPer_category
FROM dbo.[gold.dim_products]
GROUP BY category
ORDER BY AVG(cost) DESC

---What is the total revenue generated for each category? 
SELECT 
st.category,
SUM(ft.sales_amount) AS AvergeCostPer_category
FROM dbo.[gold.fact_sales] AS ft
LEFT JOIN dbo.[gold.dim_products] AS st
ON ft.product_key = st.product_key
GROUP BY st.category
ORDER BY SUM(ft.sales_amount) DESC

---Find total revenue is generated by each customer 
SELECT 
st.customer_key,
SUM(dt.sales_amount) AS total_revenue
FROM dbo.[gold.dim_customers] AS st
LEFT JOIN dbo.[gold.fact_sales] AS dt
ON st.customer_key = dt.customer_key
GROUP BY st.customer_key
ORDER BY SUM(dt.sales_amount) DESC

---What is the distribution of sold items across countries? 
SELECT 
tn.country, 
SUM(tm.quantity) AS total_sold_items
FROM dbo.[gold.dim_customers] AS tn
LEFT JOIN dbo.[gold.fact_sales] AS tm
ON tn.customer_key = tm.customer_key
GROUP BY tn.country
ORDER BY SUM(tm.quantity) DESC


