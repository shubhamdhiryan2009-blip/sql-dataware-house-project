SELECT SUM(sales_amount) AS total_sales FROM dbo.[gold.fact_sales]

--- Find how many items are sold
SELECT COUNT(quantity) AS total_items_sold FROM dbo.[gold.fact_sales]

--- Find the average selling price 
SELECT AVG(sales_amount) AS total_items_sold FROM dbo.[gold.fact_sales]

--- Find the Total number of Orders
SELECT COUNT(order_number) AS total_orders FROM dbo.[gold.fact_sales] --- repeated and non repeated customers
SELECT COUNT(DISTINCT order_number) AS total_orders_unique FROM dbo.[gold.fact_sales] --- unqiue customers atleast orderd one time 

